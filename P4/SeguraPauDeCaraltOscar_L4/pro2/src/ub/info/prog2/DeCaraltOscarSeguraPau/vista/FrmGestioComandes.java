    private void btnEliminarComandaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarComandaActionPerformed
        // TODO add your handling code here:
        int i = -1;
        for (String item : lstComandes.getSelectedValuesList()) {
            
                String elementSeleccionat = item;
                i +=1;
            try {
                controlador.esborrarComandes(i);
            } catch (MercatException ex) {
               JOptionPane.showMessageDialog(this, "Aquesta comanda no s'ha pogut eliminar perquè ja ha estat enviada ","ERROR: MERCAT EXCEPTION", JOptionPane.ERROR_MESSAGE);
            }
            
        }
        omplirModel();

    
        
        
        
        
        
    }//GEN-LAST:event_btnEliminarComandaActionPerformed
