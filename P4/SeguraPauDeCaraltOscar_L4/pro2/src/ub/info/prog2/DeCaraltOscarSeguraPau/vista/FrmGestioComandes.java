    /**
     * Mètode per a omplir l'Array de comandes que posteriorment voldrem mostrar per pantalla
     * a partir del mètode toString
     */
    private void omplirModel() {
        DefaultListModel model = new DefaultListModel();
        model.clear();
       
        List<String> s = controlador.recuperaComandes();
        for (String item : s) {
            model.addElement(item);

        }
        lstComandes.setModel(model);

        
    }


